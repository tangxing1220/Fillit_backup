#include <unistd.h>

void ft_putstrarr(char **strarr)
{
	int i;
	int j;

	i = 0;
	while (strarr[i] != '\0')
	{
		j = 0;
		while(strarr[i][j] != '\0')
		{
			write(1, &strarr[i][j], 1);
			j++;
		}
		write(1,"\n", 1);
		i++;
	}
}
